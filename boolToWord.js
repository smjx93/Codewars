// Complete the method that takes a boolean value and return
// a "Yes" string for true, or a "No" string for false.  

function boolToWord(bool) {
    return bool ? 'Yes' : 'No';
  }
  
  //This function takes a boolean argument bool 
  //   and returns the string 'Yes' if bool is true, 
  //   and 'No' if bool is false. 
    
  //   The expression 
  //   bool ? 'Yes' : 'No' 
  //   is called a ternary operator, 
  //   which is a shorthand way to write an if-else statement.